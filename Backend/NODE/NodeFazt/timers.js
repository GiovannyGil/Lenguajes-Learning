// las funciones de tiempo más comunes y usadas

// setInterval => ejecuta un código cada cierto tiempo

setInterval(()=>{
    console.log('Hello World')
}, 2000)  // ejecutara en consola el 'Hello World' cada 2 segundos

// setTimeOut espera que pase cierto tiempo para ejecutar un código

setTimeout(()=>{
    console.log('Hello World')
}, 5000) // muetsra un mensaje en consola luego de 5 segundos y se cierra la consola