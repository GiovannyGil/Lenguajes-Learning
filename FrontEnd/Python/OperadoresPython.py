# Comentario de una linea
"""
OPERADORES ARIRMETICOS
Operacion con uno / es division que da un resultado Flotante "Float"
Operacion con dos // es divicion que da un resultado exacto, un numero entero
Operacion con % es "MOD" da el residuo
Operacion con una * es multiplicacion
Operacion con dos ** es Potenciacion


OPERADORES RELACIONALES
> es mayor que
< es menot que
>= mayor o igual que
<= menor o igual que
!= diferente que
== igual , equivalente o "es lo mismo", no es resultado

OPERADORES LOGICOS
and / & / Y (conjuncion) = solo el true si ambos valores son true, de resto son false
or / O (Disyuncion)  = solo dara false si ambos valores son false, de resto son true
not (Negacion) = si es true la negacion la hace false y viveversa = False=True / True=False
"""
Num1 = 10
Num2 = 3
Resultado1 = Num1 / Num2  # Division flotante
print(Resultado1)
Resultado2 = Num1 // Num2  # Divicion entera
print(Resultado2)
Resultado3 = Num1 % Num2  #  residuo de la divicion
print(Resultado3)
Resultado4 = Num1 * Num2  # multiplicaion
print(Resultado4)
Resultado5 = Num1**Num2  # potencia}
print(Resultado5)


