/**
 * Array.forEach.js
 * 
 * este metodo recorre o hace un ciclo por cada elemento de una array
 */

const array1 = ['a', 'b', 'c'];

array1.forEach((element) => console.log(element));

// Expected output: "a"
// Expected output: "b"
// Expected output: "c"

const paises = ['Colombia', 'Canada', 'España', 'Francia', 'Rusia']

paises.forEach((element) => console.log(element))
// > "Colombia"
// > "Canada"
// > "España"
// > "Francia"
// > "Rusia"