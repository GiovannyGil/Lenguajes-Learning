/**
 * Array.unshift.js
 * 
 * es un metodo que agrega un nuevo elemento o elementos al inicio de la matriz y retorna el ultimo elemento ingresado
 */

const array1 = [1, 2, 3];

console.log(array1.unshift(4, 5));
// Expected output: 5

console.log(array1);
// Expected output: Array [4, 5, 1, 2, 3]
